# 为什么需要高阶的知识来支撑我们的代码呢？
# 编程的基础知识和高阶知识之间的关系：
# 基础知识可以让我们写出代码，但不能写出优质的可维护的代码。
# 业务逻辑的开发者，不考虑太多封装性，高阶知识用处并不大。
# 包、类库的开发者，高阶知识非常有用。
# 每一个开发者都尝试写包和类库。
# 把自己当作一个服务方，给别人提供包和类库，才能理解为什么很多编程语言都提供高阶的知识。
# 只写业务逻辑很难体会高阶语法的妙处。

