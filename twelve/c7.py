"""
12-7 命令式编程 vs 函数式编程
"""

# 命令式编程最为熟知.
# 无论度复杂的业务逻辑,控制流程的几个关键点是:
# 函数
# 条件判断
# 循环
# 这三大类是命令式编程最显著的特征.

# 函数式编程:
# 函数式编程的主要手段:
# map
# reduce
# filter
# lambda 表达式
# lambda 表达式在函数式编程里可以称作算子.
# 理论上,我们可以用以上四个手段去替换所有命令式编程的复杂逻辑.
# lambda 表达式是函数式编程最基本的单元，他在一定程度上可以替换命令式编程里的函数。
# map 和 reduce 可以替换循环。

# 建议：函数式编程可以结合到命令式编程里，帮助我们写出简洁代码，但不推荐全盘都用函数式编程构建业务逻辑。
# lisp 语言是函数式语言的鼻祖。
# Python 等只是支持函数式编程的特性，但本质还是命令式语言。
# lisp 语言在人工只能领域用得比较多。



